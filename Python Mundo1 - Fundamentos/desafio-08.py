# km hm dam m dm cm mm

metros = float(input('Quantos metros? '))
km = metros/1000
hm = metros/100
dam = metros/10
dm = metros*10
cm = metros*100
mm = metros*1000
print('Conversão em kilometros: {}km'.format(km))
print('Conversão em hectometros: {}hm'.format(hm))
print('Conversão em decametros: {}dam'.format(dam))
print('Conversão em decimetros: {}dm'.format(dm))
print('Conversão em centimetros: {}cm'.format(cm))
print('Conversão em milímetros: {}mm'.format(mm))