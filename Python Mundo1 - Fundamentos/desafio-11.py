# Área e Pintura
# 1L Tinta -- 2m^2 área
#      x   -- area

largura = float(input('Digite a largura: '))
altura = float(input('Digite a altura: '))
area = largura*altura
pintura = area/2
print('A área da parede é: {}m²'.format(area))
print('A quantidade de tinta necessária é: {}L'.format(pintura))


